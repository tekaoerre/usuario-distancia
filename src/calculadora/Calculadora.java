/**
 *  * Esta es la clase calculadora que proporciona métodos para realizar operaciones matemáticas básicas.
 * @author franj
 */

public class Calculadora 
{
 /**
     * Este es el constructor de la clase Calculadora.
     */
    public Calculadora() 
    {
        // Constructor
    }
    
    /**
     * Este método suma dos números enteros.
     * 
     * @param a el primer número entero a sumar
     * @param b el segundo número entero a sumar
     * @return la suma de los dos números
     */
    public int sumar(int a, int b) 
    {
        return a + b;
    }
    
    /**
     * Este método resta dos números enteros.
     * 
     * @param a el primer número entero (minuendo)
     * @param b el segundo número entero (sustraendo)
     * @return la resta de los dos números
     */
    public int restar(int a, int b) 
    {
        return a - b;
    }
}